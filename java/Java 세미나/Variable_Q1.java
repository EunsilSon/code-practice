public class Variable_Q1 {
    public static void main(String args[]) {
        /*
        정수형 변수 A, B, C 를 만들어 A 와 B 에 정수 값을 주고 A 와 B 를 더한 값을 C 에 준 후 A, B, C 변수를 하나의 출력문에 사용해 "[A의값] + [B의값] = [C의 값]" 을 출력하라.
        */

        int a = 3;
        int b = 4;
        int c = a + b;

        System.out.println(c);
    }
}
