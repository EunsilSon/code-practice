public class data_type_final {
    public static void main(String[] args) {

        String name = "홍길동";
        int age = 20;
        boolean student = true;
        double eye = 1.0;
        final int number = 123456789;

        System.out.println("나의 이름은: " + name);
        System.out.println("나이는: " + age);
        System.out.println("학생입니까? " + student);
        System.out.println("시력은: " + eye);
        System.out.println("학번은: " + number);

    }
}
